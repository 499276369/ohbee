
public class Configuration 
{	
	public static final int DEVICEID = 1;
	
	public static final int	PROVIDERINTERVAL = 2;
	
	public static final int PROVIDERMAXAGE = 3;
	
	public static final int PROVIDERTIMEOUT = 4;
	
	public static final int PROVIDERUPLOADURL = 5;
	
	private String deviceID;
	
	private int providerInterval = -1;
	
	private int providerTimeout = -1;
	
	private int providerMaxAge = -1;
	
	private String uploadURL;

	public String getDeviceID() 
	{
		return deviceID;
	}

	public void setDeviceID(String deviceID) 
	{
		this.deviceID = deviceID;
	}

	public int getProviderInterval() 
	{
		return providerInterval;
	}

	public void setProviderInterval(int providerInterval) 
	{
		this.providerInterval = providerInterval;
	}

	public int getProviderTimeout() 
	{
		return providerTimeout;
	}

	public void setProviderTimeout(int providerTimeout) 
	{
		this.providerTimeout = providerTimeout;
	}

	public int getProviderMaxAge() 
	{
		return providerMaxAge;
	}

	public void setProviderMaxAge(int providerMaxAge) 
	{
		this.providerMaxAge = providerMaxAge;
	}

	public String getUploadURL() 
	{
		return uploadURL;
	}

	public void setUploadURL(String uploadURL) 
	{
		this.uploadURL = uploadURL;
	}
}
