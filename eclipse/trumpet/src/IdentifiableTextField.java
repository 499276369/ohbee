import javax.microedition.lcdui.TextField;

public class IdentifiableTextField 
extends TextField
{
	private final int ID;
	
	public IdentifiableTextField(
			int ID, String label, String text, int maxSize, int constraints) 
	{
		super(label, text, maxSize, constraints);
		this.ID = ID;
	}
	
	public int getID()
	{
		return ID;
	}
}
